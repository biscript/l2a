local l2a = require "l2a"                           --  require l2a (lua 2 (to) audacity) library   --
local labels                                        --  create new local labels (nil) (optional)    --
------------------------------------------------------------------------------------------------------


------------------------------------------------------------------------------------------------------
--                                                                                                  --
--                                       --==## ACT 1 ##==--                                        --
--                                   Hot Potato from labels.lua                                     --
--                                                                                                  --
------------------------------------------------------------------------------------------------------
--                                                                                                  --
--                                         READ LUA LABELS                                          --
--                                                                                                  --
------------------------------------------------------------------------------------------------------
labels = l2a.read( "labels.lua" )                   --   read labels.lua                            --
print( labels )                                     --   print lua labels                           --
l2a.write( "./output/act1-out1.txt", labels )       --   write lua labels to o1.txt                 --
------------------------------------------------------------------------------------------------------
--                                                                                                  --
--                                      LUA LABLES TO TABELS                                        --
--                                                                                                  --
------------------------------------------------------------------------------------------------------
labels = l2a.lua2table( labels )                    --   parse datas in labels.lua to tables        --
print( labels )                                     --   print parsed tables (return memory point)  --
-- l2a.write( "./output/act1-out2.txt", labels )    --   ERROR cannot write a tables to a file      --
------------------------------------------------------------------------------------------------------
--                                                                                                  --
--                                   TABLES TO AUDACITY LABELS                                      --
--                                                                                                  --
------------------------------------------------------------------------------------------------------
labels = l2a.table2auda( labels )                   --   parse tables to audacity labels            --
print( labels )                                     --   print audacity labels                      --
l2a.write( "./output/act1-out3.txt", labels )       --   write audacity labels to o3.txt            --
------------------------------------------------------------------------------------------------------
--                                                                                                  --
--                                   AUDACITY LABLES TO TABLES                                      --
--                                                                                                  --
------------------------------------------------------------------------------------------------------
labels = l2a.auda2table( labels )                   --   parse audacity labels to tables            --
print( labels )                                     --   print parsed tables (return memory point)  --
